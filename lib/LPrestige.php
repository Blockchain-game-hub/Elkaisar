     public static function buildGainPrestige($Building)
    {
        
        if(static::$BUILDING_PRESTIGE[$Building["Type"]][$Building["Lvl"]]){
            return static::$BUILDING_PRESTIGE[$Building["Type"]][$Building["Lvl"]];
        }else{

            return 100;
        }
        
    }
    
    public static function armyGainPrestige($ArmyType ,$amount)
    {
        
        switch ($ArmyType):
            
        case "army_a":   return $amount*1.5;
            
        case "army_b":   return $amount*3;
            
        case "army_c":   return $amount*19;
            
        case "army_d":   return $amount*2;
            
        case "army_e":   return $amount*8;
            
        case "army_f":   return $amount*38;
            
        case "spies":    return $amount*3;
            
        case "wall_a":   return $amount*1.5;
            
        case "wall_b":   return $amount*9;
            
        case "wall_c":   return $amount*27;
            
        endswitch;
        
    }
    public static function jopGainPrestige($amount)
    {
        
        return $amount*1;
    }
    
    
    public static function eduGainPrestige($study , $lvlTo)
    {
        
        return static::$STUDING_PRESTIGE[$study][$lvlTo];
        
    }
    public static function addPres($idPlayer, $amount)
    {
        updateTable("prestige = prestige + :a", "player", "id_player = :idp", ["idp" => $idPlayer, "a" => $amount]);
    }
    
